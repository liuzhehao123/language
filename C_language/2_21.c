#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main()
{
	int num = 9;
	//0000 0000 0000 0000 0000 0000 0000 1001
	float* pFloat = (float*)&num;
	printf("n:%d\n", num);
	//9
	printf("*pFloat:%f\n", *pFloat);
	// 0 00000000 00000000000000000001001
	// E -126
	//S=0，M=0.00000000000000000001001 ，E=-126
	//(-1)^0 * 0.00000000000000000001001 * 2^(-126)
	//%f只保留6位
	//0.000000
	*pFloat = 9.0;
	//1001.0
	//1.001 * 2^3
	//(-1)^0 * 1.001 * 2^3
	//S=0, E=3, M=1.001
	//E+127=130 二进制1000 0010
	//0 1000 0010 00100000000000000000000
	printf("n:%d\n", num);
	//01000001000100000000000000000000
	//1091567616
	printf("*pFloat:%f\n", *pFloat);
	//9.0
	return 0;
}